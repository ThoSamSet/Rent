function main() {
  if (!fs.existsSync(pkgFiles)) {
    console.error('❌ Chưa cài @fontsource/be-vietnam-pro — chạy npm install');
    process.exit(1);
  }

  fs.mkdirSync(fontsDir, { recursive: true });

  for (const file of FONT_FILES) {
    const from = path.join(pkgFiles, file);
    const to = path.join(fontsDir, file);
    if (!fs.existsSync(from)) {
      console.error(`❌ Thiếu font: ${file}`);
      process.exit(1);
    }
    fs.copyFileSync(from, to);
  }

  fs.writeFileSync(path.join(fontsDir, 'be-vietnam-pro.css'), FONT_CSS);
  console.log(`✅ Fonts — ${FONT_FILES.length} file woff2 + be-vietnam-pro.css`);
}

main();
